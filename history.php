<script>
	$(function(){
		var modal = new LModal();
		var index = "";
		$(".history_ul").on("click",".del_a",function(){
			index = $(this).parents(".history_item").index();
			modal.showModal(".history_modal");
		})
		$(".modal_confirm_btn").click(function(){
			alert("ok"+index);
			modal.cancleModal(".history_modal")
		})
				
		var _content = []; //临时存储li循环内容
		var jq22 = {
			_default:4, //默认显示图片个数
			_loading:4,  //每次点击按钮后加载的个数
			init:function(){
				var lis = $(".history_ul li");
				$(".history_ul").html("");
				for(var n=0;n<jq22._default;n++){
					lis.eq(n).appendTo(".history_ul");
				}
				for(var i=jq22._default;i<lis.length;i++){
					_content.push(lis.eq(i));
				}
				$(".history_ul.hidden").html("");
			},
			loadMore:function(){
				var mLis = $(".history_ul li").length;
				for(var i =0;i<jq22._loading;i++){
					var target = _content.shift();
					if(!target){
						$('.load_more').html("全部加载完毕...");
						break;
					}
					$(".history_ul").append(target);
				}
			}
		}
		jq22.init();

		$(".load_more").click(function(){
			jq22.loadMore();
		})
	})
</script>
